- (IBAction)startSetupAssistant:(id)sender
{
    UIStoryboard* storyboard = self.storyboard;
    UIViewController* welcomeScreenController = [storyboard instantiateViewControllerWithIdentifier:@"WelcomeScreen"];

    [UIView transitionWithView:self.view
                      duration:1.
                       options:UIViewAnimationOptionTransitionCrossDissolve
                    animations:^{

                        self.view.window.rootViewController = welcomeScreenController;
                        [self.view.window makeKeyAndVisible];

                        [[UIApplication sharedApplication] setStatusBarHidden:YES withAnimation:UIStatusBarAnimationSlide];
                    }
                    completion:nil];
}

